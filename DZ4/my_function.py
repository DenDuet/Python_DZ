def simple_numbers(number) -> bool:
    '''
    Функция получает число и проверяет его на предмет является оно простым или нет. Выдает ответ True или False
    '''
    k = 0
    for i in range(2, (number // 2)+1):
        if (number % i == 0):
            k = k+1
    if (k <= 0):
        return True
    else:
        return False

