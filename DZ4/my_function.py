from random import randint


# _________________________________________________________________

def get_number(input_string, stop=None) -> int:
    '''
    Функция получает с клавиатуры целое с проверкой корректности ввода/
    Можно передать число stop и, тогда будет ввод больше или равно этому числу
    '''
    while True:
        try:
            num = int(input(input_string))
            while stop != None and num <= stop:
                num = int(input(input_string))
            return num
        except ValueError:
            print("Вы ввели не число. Повторите ввод!")

