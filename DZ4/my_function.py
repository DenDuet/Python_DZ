def delete_repeat_numbers(numbers) -> list:
    '''
    Функция удаляет из списка повторяющиеся символы и возвращает новый список без повторов
    '''
    numbers_list = []
    for i in numbers:
        if i not in numbers_list:
            numbers_list.append(i)
    return numbers_list

